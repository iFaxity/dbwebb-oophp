<h1>Tärningsspelet 100 version 2</h1>

<p>I detta spel ska du spela ett virtuellt tärningsspel mot servern.</p>
<p>
    Reglerna är enkla, ni turas om att slå med 6 stycken tärningar
    och varje tärningsöga motsvarar mängen poäng du fick denna rundan.
    Men om du slår en etta så återställs <strong>alla</strong> poäng du samlat denna rundan,
    och du får lämna över tärningarna till servern.
    Du kan om du vill säkra dina poäng du samlade eller slå igen för <strong>extra poäng</strong>.
</p>
<p>
    Servern är programmerad till att spela säkert men kan riskera ibland om det känns logiskt.
    Lycka till!
